<strong>1. Latar Belakang</strong> <br />
 	<p>Sejak Undang-Undang Nomor 14 Tahun 2008 Tentang Keterbukaan Informasi Publik (UU KIP) diberlakukan secara efektif pada tanggal 30 April 2010 telah mendorong bangsa Indonesia satu langkah maju ke depan, menjadi bangsa yang transparan dan akuntabel dalam mengelola sumber daya publik. UU KIP sebagai instrumen hukum yang mengikat merupakan tonggak atau dasar bagi seluruh rakyat Indonesia untuk bersama-sama mengawasi secara langsung pelayanan publik yang diselenggarakan oleh Badan Publik.</p>
<p>Keterbukaan informasi adalah salah satu pilar penting yang akan mendorong terciptanya iklim transparansi. Terlebih di era yang serba terbuka ini, keinginan masyarakat untuk memperoleh informasi semakin tinggi. Diberlakukannya UU KIP merupakan perubahan yang mendasar dalam kehidupan bermasyarakat, berbangsa dan bernegara, oleh sebab itu perlu adanya kesadaran dari seluruh elemen bangsa agar setiap lembaga dan badan pemerintah dalam pengelolaan informasi harus dengan prinsip good governance, tata kelola yang baik dan akuntabilitas.</p>
<p>Sejalan dengan amanah Undang-Undang Nomor 14 Tahun 2008 tentang Keterbukaan Informasi Publik, Kementerian Perhubungan telah melakukan beberapa upaya menyelaraskan aspek legal dengan menetapkan Peraturan Menteri Perhubungan Nomor PM. 72 Tahun 2010 Tentang Standar Prosedur Operasional Layanan Informasi Publik di lingkungan Kementerian Perhubungan, dan Instruksi Menteri Perhubungan Nomor: IM 6 Tahun 2010 tentang Langkah-langkah Pengelolaan dan Pelayanan Informasi Publik di Lingkungan Kementerian Perhubungan yang ditetapkan pada tanggal 23 Desember 2010.</p>
 	 
2	Visi dan Misi
 	Visi
 	Terwujudnya layanan informasi publik yang  profesional, transparan dan akuntabel untuk meningkatkan peran serta aktif masyarakat dalam  penyelenggaraan  pembangunan sektor  transportasi
 	1	Layanan Informasi Publik
 	 	Suatu usaha untuk memberikan informasi publik sesuai Undang- Undang No. 14 tahun 2008 tentang Keterbukaan Informasi Publik di lingkungan Kementerian Perhubungan;
 	2	Profesional
 	 	Memiliki komitmen untuk senantiasa meningkatkan layanan informasi publik;
 	3	Transparan
 	 	Memberikan akses seluar-luasnya kepada masyarakat dalam memperoleh informasi publik dengan cepat dan tepat waktu, biaya ringan, dan cara yang sederhana;
 	4	Akuntabel
 	 	Pengelolaan informasi dan dokumentasi dilakukan dengan prinsip good governance (tata kelola  pemerintahan yang baik);
 	5	Peran Aktif  Masyarakat
 	 	Memberikan kesempatan seluas-luasnya kepada masyarakat untuk berperan aktif  dalam penyelenggaraan pembangunan sektor transportasi, baik pada tingkat pengawasan pelaksanaan penyelenggaraan  maupun  pada tingkat pelibatan masyarakat dalam proses pengambilan kebijakan sektor transportasi;
 	 	 	 
 	Misi
 	1	Menjamin akses informasi publik sesuai Undang-Undang No. 14 tahun 2008 tentang Keterbukaan Informasi Publik;
 	2	Meningkatkan kualitas layanan informasi publik;
 	3	Meningkatkan profesionalisme SDM layanan  informasi publik;
 	4	Meningkatkan sarana-prasarana dalam rangka efisiensi dan efektivitas layanan informasi publik;
 	5	Meningkatkan pengelolaan informasi dan dokumentasi secara baik, efisien, mudah diakses dan bersifat desentralisasi;
 	 	 	 
